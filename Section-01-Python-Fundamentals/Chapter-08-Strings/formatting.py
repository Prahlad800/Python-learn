# Topic: String Formatting
# Explanation: String formatting makes output more structured.

# Syntax:
# name = "Asha"
print("Hello {}".format(name))

# Examples:
# name = "Asha"
city = "Delhi"
print("Hello {} from {}".format(name, city))

